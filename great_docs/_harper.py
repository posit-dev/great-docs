def extract_prose_from_markdown(content: str) -> tuple[str, dict[int, int]]:
    """
    Extract prose sections from markdown, skipping fenced code blocks and frontmatter.

    Keeps inline code (`code`) in prose for checking - only filters out
    fenced code blocks (``` ... ```) and YAML frontmatter.

    Returns the prose-only content and a mapping from prose line numbers
    to original file line numbers for accurate error reporting.

    Parameters
    ----------
    content
        Full markdown/qmd file content.

    Returns
    -------
    tuple[str, dict[int, int]]
        (prose_content, line_mapping) where line_mapping maps prose line numbers
        (1-based) to original file line numbers (1-based).
    """
    lines = content.split("\n")
    prose_lines: list[str] = []
    line_mapping: dict[int, int] = {}  # prose_line -> original_line

    in_code_block = False
    in_frontmatter = False
    frontmatter_ended = False
    code_fence_pattern = re.compile(r"^(`{3,}|~{3,})")

    for orig_line_num, line in enumerate(lines, start=1):
        stripped = line.strip()

        # Handle YAML frontmatter (only at very start of file)
        if orig_line_num == 1 and stripped == "---":
            in_frontmatter = True
            # Add placeholder to preserve line numbers
            prose_lines.append("")
            line_mapping[len(prose_lines)] = orig_line_num
            continue

        if in_frontmatter:
            if stripped == "---" and not frontmatter_ended:
                in_frontmatter = False
                frontmatter_ended = True
            # Add placeholder to preserve line numbers
            prose_lines.append("")
            line_mapping[len(prose_lines)] = orig_line_num
            continue

        # Handle fenced code blocks
        fence_match = code_fence_pattern.match(stripped)
        if fence_match:
            in_code_block = not in_code_block
            # Add placeholder to preserve line numbers
            prose_lines.append("")
            line_mapping[len(prose_lines)] = orig_line_num
            continue

        if in_code_block:
            # Add placeholder to preserve line numbers
            prose_lines.append("")
            line_mapping[len(prose_lines)] = orig_line_num
            continue

        # Keep prose lines as-is (including inline `code` - that's part of prose)
        prose_lines.append(line)
        line_mapping[len(prose_lines)] = orig_line_num

    return "\n".join(prose_lines), line_mapping


