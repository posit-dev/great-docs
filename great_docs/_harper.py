# Rules that produce too many false positives in technical documentation.
# These are designed for prose, not code/config-heavy markdown files.
DEFAULT_IGNORE_RULES_FOR_DOCS = [
    "Spaces",  # Flags YAML indentation as "multiple spaces"
    "Dashes",  # Flags `---` frontmatter and `--` CLI options
    "QuoteSpacing",  # Issues with quotes in code blocks
    "UnclosedQuotes",  # Flags strings in code blocks
    "SentenceCapitalization",  # Flags code/config lines starting lowercase
    "LongSentences",  # Code examples often have long "sentences"
    "NoFrenchSpaces",  # Flags code indentation as "French spaces"
    "OrthographicConsistency",  # Flags technical terms like yaml/YAML, css/CSS
    "EllipsisLength",  # Flags `..` in file paths and ranges
    "MoreAdjective",  # Suggests "frequentest" instead of "most frequent"
    # Technical abbreviations are fine in docs
    "ExpandArgument",  # "arg" → "argument" - Args is standard in docstrings
    "ExpandParameter",  # "param" → "parameter" - param is standard
    # Compound word rules conflict with technical writing
    "CompoundNouns",  # "file path" → "filepath", "spell checking" → "spellchecking"
    "PhrasalVerbAsCompoundNoun",  # "setup" → "set up" - both are valid
    "RoadMap",  # "roadmap" is standard in tech/business contexts
]

