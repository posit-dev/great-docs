@dataclass
class HarperLint:
    """A single lint result from Harper."""

    rule: str
    """Rule name, e.g., 'SpellCheck', 'TheirToTheyre'."""

    kind: str
    """Lint category, e.g., 'Spelling', 'Grammar', 'Capitalization'."""

    line: int
    """1-based line number."""

    column: int
    """1-based column number."""

    message: str
    """Human-readable description of the issue."""

    matched_text: str
    """The text that triggered the lint."""

    suggestions: list[str] = field(default_factory=list)
    """Suggested replacements."""

    priority: int = 0
    """Harper's priority score (higher = more important)."""

    file: str = ""
    """File path where the lint was found."""

    char_start: int = 0
    """Character offset (start) in the file."""

    char_end: int = 0
    """Character offset (end) in the file."""


@dataclass
class HarperFileResult:
    """Results from checking a single file."""

    file: str
    """File path."""

    lint_count: int
    """Number of lints found."""

    lints: list[HarperLint] = field(default_factory=list)
    """List of lints for this file."""

    error: str | None = None
    """Error message if file couldn't be checked."""


class HarperNotFoundError(Exception):
    """Raised when harper-cli is not installed."""

    pass


class HarperError(Exception):
    """Raised when harper-cli returns an error."""

    pass


def find_harper_cli() -> str | None:
    """
    Locate the harper-cli executable.

    Checks for:
    1. 'harper-cli' in PATH
    2. 'harper' in PATH (some installations use this name)
    3. ~/.cargo/bin/harper-cli (cargo install location)

    Returns
    -------
    str | None
        Path to harper-cli executable, or None if not found.
    """
    # Check PATH for harper-cli
    path = shutil.which("harper-cli")
    if path:
        return path

    # Check PATH for harper (alternative name)
    path = shutil.which("harper")
    if path:
        return path

    # Check cargo bin directory
    cargo_path = Path.home() / ".cargo" / "bin" / "harper-cli"
    if cargo_path.exists():
        return str(cargo_path)

    return None


def get_harper_version(harper_path: str | None = None) -> str | None:
    """
    Get the installed Harper version.

    Parameters
    ----------
    harper_path
        Path to harper-cli. If None, will search for it.

    Returns
    -------
    str | None
        Version string (e.g., "1.12.0"), or None if unavailable.
    """
    if harper_path is None:
        harper_path = find_harper_cli()

    if harper_path is None:
        return None

    try:
        result = subprocess.run(
            [harper_path, "--version"],
            capture_output=True,
            text=True,
            timeout=5,
        )
        if result.returncode == 0:
            # Output is like "harper-cli 1.12.0"
            version = result.stdout.strip()
            if " " in version:
                return version.split()[-1]
            return version
    except (subprocess.SubprocessError, OSError):
        pass

    return None


def run_harper(
    files: list[Path] | list[str],
    *,
    dialect: str = "us",
    user_dict_path: Path | str | None = None,
    ignore_rules: list[str] | None = None,
    only_rules: list[str] | None = None,
    harper_path: str | None = None,
    timeout: int = 120,
) -> list[HarperFileResult]:
    """
    Run harper-cli on files and parse the results.

    Parameters
    ----------
    files
        List of file paths to check.
    dialect
        English dialect: 'us', 'uk', 'au', 'in', 'ca'.
    user_dict_path
        Path to a custom dictionary file (one word per line).
    ignore_rules
        List of rule names to skip.
    only_rules
        List of rule names to run exclusively.
    harper_path
        Path to harper-cli. If None, will search for it.
    timeout
        Maximum seconds to wait for harper-cli.

    Returns
    -------
    list[HarperFileResult]
        Results for each file checked.

    Raises
    ------
    HarperNotFoundError
        If harper-cli is not installed.
    HarperError
        If harper-cli returns an unexpected error.
    """
    if harper_path is None:
        harper_path = find_harper_cli()

    if harper_path is None:
        raise HarperNotFoundError(
            "harper-cli is not installed.\n\n"
            "Install it with one of:\n"
            "  brew install harper      # macOS (Homebrew)\n"
            "  cargo install harper-cli # Any platform with Rust\n\n"
            "Or download from: https://github.com/Automattic/harper/releases"
        )

    # Build command
    cmd = [
        harper_path,
        "lint",
        "--format",
        "json",
        "--dialect",
        dialect,
    ]

    # Add custom dictionary
    if user_dict_path:
        cmd.extend(["--user-dict-path", str(user_dict_path)])

    # Add rule filters
    if ignore_rules:
        cmd.extend(["--ignore", ",".join(ignore_rules)])

    if only_rules:
        cmd.extend(["--only", ",".join(only_rules)])

    # Add files
    cmd.extend(str(f) for f in files)

    # Run harper-cli
    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
        )
    except subprocess.TimeoutExpired as e:
        raise HarperError(f"harper-cli timed out after {timeout} seconds") from e
    except OSError as e:
        raise HarperError(f"Failed to run harper-cli: {e}") from e

    # Parse JSON output (ignore stderr which has informational messages)
    stdout = result.stdout.strip()

    if not stdout:
        # No output - could be no files or an error
        if result.returncode != 0 and result.stderr:
            # Filter out the "Note:" messages about user dictionary
            stderr_lines = [
                line for line in result.stderr.strip().split("\n") if not line.startswith("Note:")
            ]
            if stderr_lines:
                raise HarperError(f"harper-cli error: {' '.join(stderr_lines)}")
        return []

    try:
        data = json.loads(stdout)
    except json.JSONDecodeError as e:
        raise HarperError(f"Failed to parse harper-cli output: {e}") from e

    # Convert to HarperFileResult objects
    results = []
    for file_data in data:
        lints = []
        for lint_data in file_data.get("lints", []):
            span = lint_data.get("span", {})
            lints.append(
                HarperLint(
                    rule=lint_data.get("rule", ""),
                    kind=lint_data.get("kind", ""),
                    line=lint_data.get("line", 0),
                    column=lint_data.get("column", 0),
                    message=lint_data.get("message", ""),
                    matched_text=lint_data.get("matched_text", ""),
                    suggestions=lint_data.get("suggestions", []),
                    priority=lint_data.get("priority", 0),
                    file=file_data.get("file", ""),
                    char_start=span.get("char_start", 0),
                    char_end=span.get("char_end", 0),
                )
            )

        results.append(
            HarperFileResult(
                file=file_data.get("file", ""),
                lint_count=file_data.get("lint_count", 0),
                lints=lints,
                error=file_data.get("error"),
            )
        )

    return results


def run_harper_on_text(
    text: str,
    *,
    dialect: str = "us",
    user_dict_path: Path | str | None = None,
    ignore_rules: list[str] | None = None,
    only_rules: list[str] | None = None,
    harper_path: str | None = None,
    timeout: int = 30,
) -> list[HarperLint]:
    """
    Run harper-cli on text content via stdin.

    This is useful for checking content that doesn't have a file extension
    Harper recognizes (like .qmd files).

    Parameters
    ----------
    text
        Text content to check.
    dialect
        English dialect: 'us', 'uk', 'au', 'in', 'ca'.
    user_dict_path
        Path to a custom dictionary file.
    ignore_rules
        List of rule names to skip.
    only_rules
        List of rule names to run exclusively.
    harper_path
        Path to harper-cli. If None, will search for it.
    timeout
        Maximum seconds to wait.

    Returns
    -------
    list[HarperLint]
        List of lints found in the text.

    Raises
    ------
    HarperNotFoundError
        If harper-cli is not installed.
    HarperError
        If harper-cli returns an error.
    """
    if harper_path is None:
        harper_path = find_harper_cli()

    if harper_path is None:
        raise HarperNotFoundError(
            "harper-cli is not installed.\n\n"
            "Install it with one of:\n"
            "  brew install harper      # macOS (Homebrew)\n"
            "  cargo install harper-cli # Any platform with Rust\n\n"
            "Or download from: https://github.com/Automattic/harper/releases"
        )

    # Build command
    cmd = [
        harper_path,
        "lint",
        "--format",
        "json",
        "--dialect",
        dialect,
    ]

    # Add custom dictionary
    if user_dict_path:
        cmd.extend(["--user-dict-path", str(user_dict_path)])

    # Add rule filters
    if ignore_rules:
        cmd.extend(["--ignore", ",".join(ignore_rules)])

    if only_rules:
        cmd.extend(["--only", ",".join(only_rules)])

    # Run harper-cli with stdin
    try:
        result = subprocess.run(
            cmd,
            input=text,
            capture_output=True,
            text=True,
            timeout=timeout,
        )
    except subprocess.TimeoutExpired as e:
        raise HarperError(f"harper-cli timed out after {timeout} seconds") from e
    except OSError as e:
        raise HarperError(f"Failed to run harper-cli: {e}") from e

    # Parse JSON output
    stdout = result.stdout.strip()

    if not stdout:
        return []

    try:
        data = json.loads(stdout)
    except json.JSONDecodeError as e:
        raise HarperError(f"Failed to parse harper-cli output: {e}") from e

    # Extract lints from the single result
    lints = []
    if data and len(data) > 0:
        for lint_data in data[0].get("lints", []):
            span = lint_data.get("span", {})
            lints.append(
                HarperLint(
                    rule=lint_data.get("rule", ""),
                    kind=lint_data.get("kind", ""),
                    line=lint_data.get("line", 0),
                    column=lint_data.get("column", 0),
                    message=lint_data.get("message", ""),
                    matched_text=lint_data.get("matched_text", ""),
                    suggestions=lint_data.get("suggestions", []),
                    priority=lint_data.get("priority", 0),
                    file="<stdin>",
                    char_start=span.get("char_start", 0),
                    char_end=span.get("char_end", 0),
                )
            )

    return lints


def check_harper_available() -> tuple[bool, str | None]:
    """
    Check if harper-cli is available and return version info.

    Returns
    -------
    tuple[bool, str | None]
        (is_available, version_string or error_message)
    """
    path = find_harper_cli()
    if path is None:
        return False, (
            "harper-cli is not installed.\n"
            "Install with: brew install harper (macOS) or cargo install harper-cli"
        )

    version = get_harper_version(path)
    if version:
        return True, f"harper-cli {version}"
    else:
        return True, f"harper-cli (version unknown) at {path}"
